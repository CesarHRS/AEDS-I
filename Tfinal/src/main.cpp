pair<int, int> findStartPosition(vector<vector<char>> &matrix) {
    int rows = matrix.size();
    int cols = matrix[0].size();
    pair<int, int> start_point;

    for (int i = 0; i < rows; ++i) {
        for (int j = 0; j < cols; ++j) {
            if (matrix[i][j] == 'I') {
                start_point.first = i;
                start_point.second = j;
                return start_point;
            }
        }
    }
    return start_point;
}

pair<int, int> findEndPosition(vector<vector<char>> &matrix) {
    int rows = matrix.size();
    int cols = matrix[0].size();
    pair<int, int> end_point;

    for (int i = 0; i < rows; ++i) {
        for (int j = 0; j < cols; ++j) {
            if (matrix[i][j] == 'F') {
                end_point.first = i;
                end_point.second = j;
                return end_point;
            }
        }
    }
    return end_point;
}


